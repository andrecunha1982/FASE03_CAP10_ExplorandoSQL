-- Tabela CULTURAS
CREATE TABLE EXPSQL_CULTURAS (
    ID_CULTURA NUMBER PRIMARY KEY NOT NULL,
    CULTURA VARCHAR2(100) NOT NULL,
    TIPO_CULTURA VARCHAR2(100) NOT NULL
);

-- Tabela REGIAO
CREATE TABLE EXPSQL_REGIAO (
    ID_ESTADO NUMBER PRIMARY KEY NOT NULL,
    ESTADO VARCHAR2(100) NOT NULL,
    REGIAO VARCHAR2(100) NOT NULL
);

-- Tabela VARIAVEIS
CREATE TABLE EXPSQL_VARIAVEIS (
    ID_VARIAVEL NUMBER PRIMARY KEY NOT NULL,
    VARIAVEL VARCHAR2(100) NOT NULL,
    METRICA_VARIAVEL VARCHAR2(100) NOT NULL
);

-- Tabela EREGISTRO
CREATE TABLE EXPSQL_REGISTRO (
    ID_REGISTRO NUMBER PRIMARY KEY NOT NULL,
    ID_CULTURA NUMBER NOT NULL,
    ID_ESTADO NUMBER NOT NULL,
    ID_VARIAVEL NUMBER NOT NULL,
    PERIODO DATE NOT NULL,
    VALOR FLOAT,
    CONSTRAINT FK_ID_CULTURA FOREIGN KEY (ID_CULTURA) REFERENCES EXPSQL_CULTURAS(ID_CULTURA),
    CONSTRAINT FK_ID_ESTADO FOREIGN KEY (ID_ESTADO) REFERENCES EXPSQL_REGIAO(ID_ESTADO),
    CONSTRAINT FK_ID_VARIAVEL FOREIGN KEY (ID_VARIAVEL) REFERENCES EXPSQL_VARIAVEIS(ID_VARIAVEL)
);
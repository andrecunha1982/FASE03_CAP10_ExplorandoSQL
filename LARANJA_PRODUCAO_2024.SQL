SELECT  T2."CULTURA",
        T3."ESTADO",
        SUM(T1."VALOR") AS "PRODUCAO_TONELADAS"
FROM EXPSQL_REGISTRO T1
JOIN ADMIN.EXPSQL_CULTURAS T2 ON T1."ID_CULTURA" = T2."ID_CULTURA"
JOIN ADMIN.EXPSQL_REGIAO T3 ON T3."ID_ESTADO" = T1."ID_ESTADO"
JOIN ADMIN.EXPSQL_VARIAVEIS T4 ON T4."ID_VARIAVEL" = T1."ID_VARIAVEL"
WHERE T2."CULTURA" = 'Laranja' AND T4."VARIAVEL" = 'Produção' AND TO_CHAR(TO_DATE(T1."PERIODO", 'DD/MM/YYYY, HH24:MI:SS'), 'YYYY') = '0024'
GROUP BY T2."CULTURA",T3."ESTADO"
ORDER BY "PRODUCAO_TONELADAS" DESC
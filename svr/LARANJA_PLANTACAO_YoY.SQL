/* AREA EM HECTARES PLANTADA DE LARANJA ANO A ANO*/

SELECT  T2."CULTURA",
        TO_CHAR(TO_DATE(T1."PERIODO", 'DD-MON-RR'), 'YYYY') AS "ANO",
        SUM(T1."VALOR") AS "PLANTACAO_HECTARES"
FROM EXPSQL_REGISTRO T1
JOIN ADMIN.EXPSQL_CULTURAS T2 ON T1."ID_CULTURA" = T2."ID_CULTURA"
JOIN ADMIN.EXPSQL_REGIAO T3 ON T3."ID_ESTADO" = T1."ID_ESTADO"
JOIN ADMIN.EXPSQL_VARIAVEIS T4 ON T4."ID_VARIAVEL" = T1."ID_VARIAVEL"
WHERE T2."CULTURA" = 'Laranja' AND T4."VARIAVEL" = '√Årea plantada'
GROUP BY T2."CULTURA",TO_CHAR(TO_DATE(T1."PERIODO", 'DD-MON-RR'), 'YYYY')
ORDER BY "ANO"